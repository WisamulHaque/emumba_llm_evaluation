"""
Consistency Evaluator

Verifies that an LLM application produces consistent outputs when the same
query is submitted multiple times. Responses do not need to be word-for-word
identical, but they must agree on facts and maintain a similar quality level.

"""

import os
import sys

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))))

from typing import List, Dict, Any
from examples.common.llm_judge import LLMJudge


EVAL_PROMPT = """Purpose:
You are evaluating whether an AI system produces consistent outputs when the \
same query is submitted multiple times. Responses do not need to be \
word-for-word identical, but they must agree on facts and maintain similar quality.

Following inputs will be provided:

Inputs:
- Query: The exact question submitted to the AI system multiple times
- Responses: Multiple responses generated by the system for that same query

Query:
{query}

Responses:
{responses_block}

Based on the above-provided inputs, use the rules below to evaluate consistency:

Evaluation Rules:
1. Responses must not contradict each other on any facts or claims
2. Responses must be at a comparable level of helpfulness and completeness —
   one response must not be detailed while another is vague or unhelpful for the Query
3. If contradictions or quality gaps exist, describe each one specifically \
(e.g. "Response 2 says X while Response 1 says Y")
4. Minor wording differences or stylistic variation are acceptable — only flag \
meaningful factual or quality gaps

Output Format:
Respond with ONLY a JSON object in this exact format, no explanation outside the JSON:
{{
    "passed": <true if responses are consistent in both facts and quality, false otherwise>,
    "reason": "<brief overall summary>",
    "contradictions": ["<contradiction or quality gap description>", ...]
}}

"""


def evaluate_consistency(judge: LLMJudge, query: str, responses: List[str]) -> Dict[str, Any]:
    responses_block = "\n\n".join(f"[Response {i}]: {r}" for i, r in enumerate(responses, 1))
    prompt = EVAL_PROMPT.format(query=query, responses_block=responses_block)
    result = judge.judge(prompt)

    contradictions = result.get("contradictions", [])

    return {
        "passed": bool(result.get("passed", False)),
        "reason": result.get("reason", ""),
        "contradictions": contradictions if isinstance(contradictions, list) else [],
    }


# Test data
SAMPLES = [
    {
        # Same query, both responses agree on facts and are similar in quality (expected: PASS)
        "query_id": 1,
        "query": "What is a Python decorator?",
        "responses": [
            "A Python decorator is a function that wraps another function to modify its behavior without changing its source code. It uses the @decorator syntax and is commonly used for logging, authentication, and caching.",
            "Decorators in Python are functions that take another function as input and extend or modify its behavior. They are applied using the @ symbol above a function definition and are widely used for cross-cutting concerns like logging and access control.",
        ],
    },
    {
        # Same query, responses contradict each other on a key fact (expected: FAIL)
        "query_id": 2,
        "query": "Does Python support multi-threading for CPU-bound tasks?",
        "responses": [
            "Yes, Python's threading module allows you to run multiple threads simultaneously, which is effective for CPU-bound tasks and can significantly speed up computation.",
            "No, Python's Global Interpreter Lock (GIL) prevents true parallel execution of threads, making multi-threading ineffective for CPU-bound tasks. Use multiprocessing instead.",
        ],
    },
    {
        # Same query, one response is detailed while the other is vague (expected: FAIL)
        "query_id": 3,
        "query": "How do database indexes work?",
        "responses": [
            "Database indexes work by creating a separate data structure (commonly a B-tree) that maps column values to their row locations. This allows the database engine to find matching rows without scanning the entire table, dramatically reducing query time for large datasets.",
            "Indexes make queries faster.",
        ],
    },
]


def main():
    from dotenv import load_dotenv
    load_dotenv()

    judge = LLMJudge(
        provider=os.getenv("LLM_JUDGE_PROVIDER", "groq"),
        model=os.getenv("LLM_JUDGE_MODEL", "meta-llama/llama-4-scout-17b-16e-instruct"),
        api_key=os.getenv("LLM_JUDGE_API_KEY"),
    )

    for sample in SAMPLES:
        result = evaluate_consistency(judge, sample["query"], sample["responses"])
        status = "PASS" if result["passed"] else "FAIL"
        print(f"\n[{sample['query_id']}] {status}")
        print(f"  Query         : {sample['query']}")
        print(f"  Responses     : {sample['responses']}")
        print(f"  Reason        : {result['reason']}")
        if result["contradictions"]:
            for c in result["contradictions"]:
                print(f"  Contradiction : {c}")
    print()


if __name__ == "__main__":
    main()